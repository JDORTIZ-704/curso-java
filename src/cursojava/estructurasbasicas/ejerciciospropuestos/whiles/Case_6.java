package cursojava.estructurasbasicas.ejerciciospropuestos.whiles;

public class Case_6 {

    public static void main (String[] args) {

        // Caso #6 -> Mostrar los números del 1 al 100 usando while.

        System.out.println("Caso #6 -> Mostrar los números del 1 al 100 usando while.");

        int numero = 1;

        while (numero <= 100) {
            System.out.println(numero);
            numero++;
        }

    }
}
